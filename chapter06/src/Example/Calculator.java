package Example;

public class Calculator {
	
	int plus(int x, int y) {
		int result = 0;
		result = x + y;
		return result;
	}
	
	int minus(int x, int y) {
		int result = 0;
		result = x - y;
		return result;
	}
	
	double divide(double x, double y) {
		double result = 0;
		result = x/y;
		return result;
	}
	int A(int x, int y) {
		int result = 0;
		result = x*y;
		return result;
	}
		
}
