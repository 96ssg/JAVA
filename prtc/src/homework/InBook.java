public class InBook {
	// 필드
	private String name;
	private String person;

	// 생성자
	public InBook(String name, String person) {
	 this.name = name;
	 this.person = person;
	 
 }

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPerson() {
		return person;
	}

	public void setPerson(String person) {
		this.person = person;
	}
	
	// 메소드

}
